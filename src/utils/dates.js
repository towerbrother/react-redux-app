const getCurrentMonth = () => {
  const month = new Date().getMonth() + 1;
  if (month < 10) {
    return `0${month}`;
  } else {
    return month;
  }
};

const getCurrentDay = () => {
  const day = new Date().getDate();
  if (day < 10) {
    return `0${day}`;
  } else {
    return day;
  }
};

export const getDate = (n = 0) =>
  `${new Date().getFullYear() + n}-${getCurrentMonth()}-${getCurrentDay()}`;
